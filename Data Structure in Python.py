
# Create a tuple of 5 city names and print each city using a loop.
# Tuple in Python

city=('Lahore','Okara','Sialkot','Shakargarh','Narowal')
for i in range(5):
    print(city[i])

# List in Python

city=['Lahore','Okara','Sialkot','Shakargarh','Narowal']
for i in range(5):
    print(city[i])


# Set in Python

city={'Lahore','Okara','Sialkot','Shakargarh','Narowal'}
for c in city:
    print(c)

# Data Dictionary 
dic={'a':10,'b':20,'c':'Zeeshan','d':'Ali'}
for c in dic:
    print(dic[c])